int	ft_isalpha(int c)
{
	unsigned char	a;

	a = (unsigned char) c;
	if ((a >= 'a') && (a <= 'z'))
		return (1);
	if ((a >= 'A') && (a <= 'Z'))
		return (1);
	return (0);
}
